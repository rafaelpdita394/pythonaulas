# Definindo a variável com a quantia em reais
quantia_em_reais = 100  # exemplo de 100 reais

# Definindo a taxa de câmbio
taxa_de_cambio = 0.69  # 0.69 BRL para 1 CNY

# Calculando o valor equivalente em yuan
quantia_em_yuan = quantia_em_reais * taxa_de_cambio

# Imprimindo o resultado
print(f"Valor em yuan chinês (CNY): {quantia_em_yuan:.2f}")
